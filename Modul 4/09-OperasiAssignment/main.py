# Operasi yang dapat dilakukan dengan meringkas
# Operasi ditambah dengan assignment

a = 5 # assignment
print("nilai a = ", a)

a += 1 # artinya a = a + 1
print("nilai a += 1, nilai a menjadi = ", a)

a -= 1 # artinya a = a - 1
print("nilai a -= 1, nilai a menjadi = ", a)

a *= 5 # artinya a = a * 5
print("nilai a *= 1, nilai a menjadi = ", a)

a /= 5 # artinya a = a / 5
print("nilai a /= 5, nilai a menjadi = ", a)

# modulus dan floor division
# %= , //=
a = 10  # 
a %= 5  
print("nilai a %= 5, nilai a menjadi = ", a) 
a = 15 
a //= 5  
print("nilai a //= 5, nilai a menjadi = ", a) 

# pangkat / exponen
# **=
a = 2  
a **= 5  
print("nilai a **= 5, nilai a menjadi = ", a)

# OR
c = True
print("nilai c = ",c)
c |= False # artinya c = c or false
print("nilai c |= false, menjadi c = ", c)
c |= True # artinya c = c or true
print("nilai c |= true, menjadi c = ", c)

# And
c = True
print("nilai c = ",c)
c &= False # artinya c = c and false
print("nilai &= false , menjadi c = ", c)






 
